-- CREATE A VIEW FOR TOP INSTRUCTOR

/* ((INSTRUCTOR JOIN DEPARTMENT ) SALARY > 350001)   ----> THIS IS RESISTRICTION(WHERE) 

{ DEPT_BUDGET,INSTRUCTOR SALARY,DEPT_NAME,INST_ID, INST_NAME} -----> PROJECTION(SELECT) 
 */ 

CREATE VIEW Top_Instructor AS SELECT DEPARTMENT.DEPT_BUDGET, INSTRUCTOR.SALARY, INSTRUCTOR.DEPT_NAME, INSTRUCTOR.INST_ID, INSTRUCTOR.inst_name FROM INSTRUCTOR JOIN DEPARTMENT ON INSTRUCTOR.DEPT_NAME=DEPARTMENT .DEPT_NAME WHERE INSTRUCTOR.SALARY > 350001; 

SELECT * FROM TOP_INSTRUCTOR; 

-- CREATE A VIEW FOR TOP EMPLOYEE

CREATE VIEW TOP_EMPLOYEE AS SELECT EMPLOYEE.EMP_NAME, EMPLOYEE.EMP_ID, EMPLOYEE.SALARY,DEPARTMENT.DEPT_BUDGET,DEPARTMENT.DEPT_BLOCK FROM EMPLOYEE JOIN DEPARTMENT ON  EMPLOYEE.DEPT_NAME=DEPARTMENT.DEPT_NAME WHERE EMPLOYEE.SALARY > 80000 ;

SELECT * FROM TOP_EMPLOYEE; 
