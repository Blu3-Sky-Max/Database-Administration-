SELECT * 
FROM SHIPMENTS 
JOIN
SUPPLIERS ON SHIPMENTS.SUP_NO=SUPPLIERS.SUP_NO
JOIN
PARTS ON SHIPMENTS.PT_NO=PARTS.PT_NO
JOIN
PROJECT ON SHIPMENTS.PJ_NO=PROJECT.PJ_NO; 


CREATE VIEW CONSOLE_PROJECT 
AS 

SELECT 
 SUPPLIERS.SUP_NAME,
 SUPPLIERS.SUP_STATUS,
 PARTS.PT_NAME,
 PARTS.PT_WEIGHT,
 PROJECT.PJ_NAME,
 PROJECT.PJ_CITY
FROM SHIPMENTS
JOIN
SUPPLIERS ON SHIPMENTS.SUP_NO=SUPPLIERS.SUP_NO
JOIN
PARTS ON SHIPMENTS.PT_NO=PARTS.PT_NO
JOIN
PROJECT ON SHIPMENTS.PJ_NO=PROJECT.PJ_NO
WHERE PROJECT.PJ_NAME='CONSOLE';
SELECT * FROM CONSOLE_PROJECT; 


CREATE VIEW AGGREGATE_PER_SUPPLIERS
AS
SELECT
    SUPPLIERS.SUP_NAME,
    SUM(SHIPMENTS.QTY) AS TOTAL_QTY
FROM 
SHIPMENTS
JOIN 
SUPPLIERS ON SHIPMENTS.SUP_NO = SUPPLIERS.SUP_NO
GROUP BY SUPPLIERS.SUP_NAME;

SELECT * FROM AGGREGATE_PER_SUPPLIERS;
