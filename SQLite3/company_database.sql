DROP TABLE IF EXISTS INSTRUCTOR;
DROP TABLE IF EXISTS EMPLOYEE; 
DROP TABLE IF EXISTS DEPARTMENT;
DROP VIEW  IF EXISTS TOP_INSTRUCTOR;
DROP VIEW IF  EXISTS TOP_EMPLOYEE;
.headers on
.mode column 
.mode table      

--Note: .mode table and other works depend on your sqlite version 
CREATE TABLE INSTRUCTOR(
	INST_ID                CHAR(5) PRIMARY KEY,
	INST_NAME           VARCHAR(20),
	DEPT_NAME                 VARCHAR(20),
	SALARY                    NUMERIC(6,2)
);

CREATE TABLE EMPLOYEE ( 
       EMP_ID INTEGER PRIMARY KEY, 

       DEPT_NAME   CHAR,

       SALARY NUMERIC (6,3), 
       EMP_NAME VARCHAR(20) 
); 

CREATE TABLE DEPARTMENT(  
	DEPT_NAME CHAR(20) PRIMARY KEY,
	DEPT_BUDGET NUMERIC (8,2), 
	DEPT_BLOCK  TEXT 
); 

